<div class="subHeader">Have a Plan</div><br />
The most important aspect of fire safety is to have a plan of action when a fire is discovered. This plan should cover the steps required to save lives and property.<br /><br />
<b>Step 1</b>: Immediate Rescue - Check to see if anyone is in danger or needs rescue<br />
<b>Step 2</b>: Confine the Fire if Possible - If the fire is in the early stage and poses no immediate threat to your personal safety, make an effort to prevent its spread by closing the door to the fire room, putting lids on burning pans or shutting off the electricity. For small cooking fires, throwing baking soda at the base of the flames will smother the fire.<br />
<b>Step 3</b>: Call for Help -  Dialing 911 will connect you with an emergency operator.<br />
<b>Step 4</b>: Contain or Extinguish - If you have the proper extinguisher, you may be able to contain or control the fire. This depends on the size, type and location of the fire. Be certain that you leave a path of retreat from the scene to guarantee your personal safety. <br />

<br /><br />
<div class="subHeader">Helpful Hints</div><br />
<ul>
 <li> Powder in dry chemical units has a tendency to compact, this is especially true when the fire extinguisher is stored in a motor vehicle. Periodic shaking of the extinguisher will keep the powder loose and aid in its performance.
 <li> Powder in dry chemical units may damage some sensitive equipment. Avoid direct contact with skin and eyes.
 <li> Some carbon tetrachloride pump-type units are still in existence. This type of extinguisher is extremely dangerous and should not be used. Check with your local fire department for disposal recommendations.
 <li> Place the emergency contact telephone number  ( 911  ) or check your local listing for departments near you.
</ul>

<br /><br />
<div class="subHeader">Remember</div><br />

Be prepared for a fire, plan your course of action beforehand.  Post an emergency escape plan, then practice it often. A portable fire extinguisher is only a first-aid or emergency unit. It can be used on small fires and only during the initial stage.  The discharge time on most fire extinguishers is very short, it can be measured in only seconds!  Regardless of your actions be sure you or somebody else notifies the Fire Department.  Make sure you use the proper Class of fire extinguisher for the hazard you are trying to extinguish.  Do not risk your life or the lives of others in fighting a fire that has grown too large for the extinguisher to put out.  Always be sure you have a clear escape path, don't get trapped fighting a fire!  If you are unsure how to operate a fire extinguisher, get out.  Saving your life and others comes first! Be proactive, use fire prevention measures in the home and at work everyday

