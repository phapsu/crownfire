<div class="subHeader">Installation</div><br />
Place the mounting bracket for the extinguisher on a firm surface 31/2 to 5 feet above the floor. Extinguishers should be installed away from any potential fire hazards and near exits or escape routes in the areas you plan to protect.

<br /><br />
<div class="subHeader">Inspection and Maintenance</div><br />
Inspect extinguishers at least once a month. Check that each is in its designated location and at the proper pressure.  The tamper seal should not be broken and the unit should not have any readily apparent damage.  Check the discharge hose for damage and the nozzle for obstructions. Most pressurized units sold today have a gauge that shows whether the unit has sufficient stored pressure to discharge the contents. If the pressure gauge shows insufficient pressure, the extinguisher should be recharged or replaced immediately. Extinguishers also should be recharged after each use regardless of the amount of chemical discharged.   Most fire extinguisher can be serviced or repaired, <a href="contact-us.html">contact Crownfire</a> to see how we can help repair any equiptment you may have.

