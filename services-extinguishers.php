Each year, fire kills or severely injures thousands of people and destroys millions of dollars worth of property. Many fires start small, and may be extinguished or controlled by a fire extinguisher. You can minimize property damage by having the right type of fire extinguisher and knowing how to use it.
Fires are classified by the types of materials involved in the fire. Most fires fit into one or a combination of Four classifications: Class A, Class B, Class C and Class D. The chart in Figure 1 will help you classify the fires that you might encounter in your facility. It gives general recommendations for the types of extinguishers most likely to be used to extinguish these fires.
