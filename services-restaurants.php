<b>Why change my system?</b><br /><br />
Because UL has developed a stringent new standard - Fire Extinguishing Systems for Protection of Restaurant Cooking Areas - that reflects the increased threat of fire in commercial cooking equipment. Systems manufactured after November 21, 1994, must meet the new standard in order to carry the UL listing label. In most jurisdictions, systems will have to be upgraded to meet the new UL300/ULC1254.6 Standard. NDPA 17, 17A & 96 now require that all systems installed in commercial kitchen system operations meet the UL300/ULC1254.6 testing criteria.<br /><br />
<b>How have the standards changed?</b><br /><br />
For example, under the old standard, tests could be conducted using mock-ups of appliances. The new standards require that testing be done using "commercially available" appliances. They also require new types of high-temperature cooking oils rather than traditional animal fats be used. These and other major changes have dramatically increased the level of performance required to pass the tests and receive a UL listing. Systems manufactured prior to the UL300/ULC1254.6 effective date could not meet the new standards.<br /><br />
<b>What Caused the Changes?</b><br /><br />
There are two primary reasons: development of high efficiency appliances and a switch to vegetable cooking oils in recent years. The results have been increased threat of fires and the occurance of fires that are harder to extinguish. Higher temperatures and reduced effectiveness of pre-UL300/ULC1254.6 systems result in fires that reignite spontaneously, in some cases, repeatedly over a period of time.<br /><br />

<b>Why does that occur?</b><br /><br />
Many of the newer appliances, for example, require less energy because they are better insulated and help cooking oils retain heat longer. If fire occurs, however, the same effect may keep oils above the auto-ignition point longer than in older appliances.<br /><br />
The oils, too, contribute to the increased fire hazard. Traditionally animal fats were used as a cooking medium. Today, many cooking operations have changed to vegetable oils because they offer greater health benefits. The oils, however, are typically used at higher cooking temperatures. Auto-ignition temperature is higher as well, making fires more difficult to extinguish.<br /><br />
<b>Are There Any Other Factors?</b><br /><br />
Another characteristic of animal fats is that they contain relatively high levels of fatty acids when compared to vegetable oils. Extinguishing agents are typically alkaline in makeup. When they are discharged on a fire and come in contact with the fatty acids, a soap-like substance forms and blankets the surface.<br /><br />
This process, called saponification, helps smother the fire and keeps the fire extinguished until the fat has cooled below its auto-ignition temperature. Vegetable oils, however, have significantly lower levels of fatty acids. Saponification is reduced, while at the same time, the higher temperature of the cooking oil helps break down the soapy layer faster.<br /><br />
<b>How Does UL300/ULC1254.6 Contribute?</b><br /><br />
The test conditions under UL300/ULC1254-6 more closely resemble the new generation of hazards. For example, the heat source must now remain on for as long as 2 minutes during the pre-burn period before the system is actuated. That keeps cooking oils at higher temperatures simulating more persistent fires. Systems that meet the new standard and are listed under UL300/ULC1254.6 have greater capability to protect against the new generation of hazards.<br /><br />

<b>Are All Types of Systems Affected Equally?</b><br /><br />
To date, no dry chemical systems have been recertified and they don't carry the UL listing label. Most liquid systems will need to be modified since many appliances require more agent than required under the old standard. <br /><br />
<b>What About Hood and Duct Protection?</b><br /><br />
Testing under UL300/ULC1254.6 is no different than under the previous standard. Existing protection may still qualify is used as part of an upgraded system. <br /><br />
<b>What is The UL300/ULC1254.6 Upgrade Process?</b><br /><br />
We will survey the system you currently use and provide an upgrade cost estimate. Then any new hardware that's required will be installed and connected to any existing equipment which can be re-used. All necessary piping and/or detection system upgrades will be included. When installation is finished, a complete functional test will be done. <br /><br />
<b>How Long Will the Upgrade Take?</b><br /><br />
Depending upon the extent of the upgrade and the age and condition of the existing system, a new UL300/ULC1254.6 upgrade will take less time than a complete new installation. Many upgrades will take less than a day. Some will take longer. We will assist you in scheduling the upgrade to best fit your operation schedule with the least amount of disruption. He can even arrange "after hours" installation to avoid loss of revenue opportunities. <br /><br />
